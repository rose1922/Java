package com.class3;

public class ShorthandOperators {
	
	public static void main(String[] args) {
		int num=120;
		num+=100;
		System.out.println(num);
		
		int num1=98;
		num1-=67;
		
		System.out.println(num1);
		int num2=10;
		num2=num2-5;
		System.out.println(num2);
		int num3=1000;
		num3+=1000;
		System.out.println(num3);
		int num4=50;
		num4+=20;
		System.out.println(num4);
		
		num4-=30;//num4=70-30
		System.out.println(num4);
		
		num4/=10;//num4=40/10
		System.out.println(num4);
		
		num4%=2;num4=4%2;
		System.out.println(num4);
		
				
				
		
	
	
	
	double cake =11;
	cake/=4;
	System.out.println(cake);
	
	/* Declare variable cakePiece=25
	 * and divide cakePiece between 7 people
	 * using shorthand operator found out
	 * how many pieces of cake left
	 * after it was distributed equally among 7 people
	 */
	
	int cakePiece2=25;
	cakePiece2%=7;
	System.out.println(cakePiece2);
	
			
}	
	
	
	
	
     
}
